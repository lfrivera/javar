framing <- function() {
	
	A = c("1", "2", "3") 
	B = c("a", "b", "c") 
	C = c(T, F, T)
	D = c(1, 2, 3)
	E = c("Z", "Y", "X")
	df = data.frame(A, B, C, D, E)       # df is a data frame

	return(df)
}






